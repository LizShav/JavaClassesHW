package class3;

public class E3Division {
    public static void main(String[] args) {
/*
we can also just use sout and it will give us answers
 */
        System.out.println(10/5);
        //if the data type is Int we will not see the decimal part
        System.out.println(11/5);
        //if the data type will be double/float(data type) that we'll see the decimal part in an answer
        System.out.println(11.0/5.0);
        int number1=10;
        int number2=4;
        double num1=10;
        double num2=4;
        System.out.println(num1/num2);
        System.out.println(number1/number2);
    }
}
