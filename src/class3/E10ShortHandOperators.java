package class3;

public class E10ShortHandOperators {
    public static void main(String[] args) {

        int num=10;
        num+=90; // value 100
        num-=50;// from 100 please subtract 50=>50
        num*=2; // after the value in line 8 was 50 we multiplied by 2=>100
        num/=10;//picked up the previous line value and divided by 10 =>10

        System.out.println(num);


    }
}
