package class7;

public class Hw3Saturday {
    public static void main(String[] args) {
        //Task3 Print even numbers from 20 to 1 (2 ways)
        int e=20;
        while(e>=1){
            System.out.print(e+ " ");
            e-=2;
        }
        System.out.println();
        for(e=20; e>=1; e-=2){
            System.out.print(e+ " ");
        }



    }
}
