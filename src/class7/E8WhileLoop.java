package class7;

public class E8WhileLoop {
    public static void main(String[] args) {
        //print number from 10 to 1 using a while loop
        int num=10;
        while(num>=1) {
            System.out.println(num);
            num--; //to print from the bigger num to the lower we use -- decreasing by 1
        }
    }
}
