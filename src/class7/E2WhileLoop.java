package class7;

public class E2WhileLoop {
    public static void main(String[] args) {
        int number=0;
        //below we are using increment operator++
        number++;//this will increase number by 1, o->1
        number++;//1->2
        System.out.println(number);

        //below we using another increment operator -- to subtract
        int num2=10;
        num2--;//decreases the value by 1
        System.out.println(num2);
    }
}
