package class7;

public class E14ForLoop {
    public static void main(String[] args) {

        int i=1;
        while(i<5){//will print 1-4.While loop its done line by line
            System.out.println(i);
            i++;
        }

        for(int j=1;j<5;j++){//will print 1-4-this is easier then doing while loop , done by using in a single line
            System.out.println(j);
        }
    }
}
