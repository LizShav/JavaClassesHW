package class2;

public class BooleanDemo {
    /*
    Boolean is the data type to store ONLY true/false , no special characters like numbers
     or dashes or question marks. ex is it raining = false
    are you hungry=true
     */
    public static void main(String[] args) {
        boolean isItRaining=false;
        System.out.println(isItRaining);
        boolean areYouHungry=true;
        System.out.println(areYouHungry);
        boolean areYouExcited=true;
        System.out.println(areYouExcited);
        boolean areYouWorkingSunday=false;
        System.out.println(areYouWorkingSunday);



    }
}
