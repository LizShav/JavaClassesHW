/*
OR(||)-operator helps combine multiple conditions
if either conditions is true it will be executed as true , if both conditions are false then will be false
 */
public class E6LogicalOperators {
    public static void main(String[] args) {

        //System.out.println((true ||true||false));
        int age=17;
        if(age <18 || age>60)
        {
            System.out.println("You will get a discount");
        }



    }
}
