package class9;

public class E3Arrays {
    public static void main(String[] args) {

        double [] numbers={10.5,5.5,0.05,100.5,586};

        int i=0;//this just adds the variable with assigned value 0, which we then mention in and array[0]-which can be int i
        System.out.println(numbers[i]);
        i=1;//integers are also indexes (numbers 0,1,2,3etc...)
        System.out.println(numbers[1]);
        i=2;
        System.out.println(numbers[2]);
        i=3;
        System.out.println(numbers[3]);
        i=4;
        System.out.println(numbers[4]);
    }
}
